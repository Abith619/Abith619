import { patch } from "@web/core/utils/patch";
import { PaymentScreen } from "@point_of_sale/app/screens/payment_screen/payment_screen";
import { onMounted } from "@odoo/owl";
import { isFiscalPrinterActive } from "./helpers/utils";

patch(PaymentScreen.prototype, {
    setup() {
        super.setup(...arguments);
        onMounted(() => {
            if (isFiscalPrinterActive(this.pos.config)) {
                const order = this.pos.get_order();
                const totalToPay = this.env.utils.formatCurrency(order.getTotalDue(), false);
                this.pos.fiscalPrinter.displayText(`To Pay: ${totalToPay}`);
            }
        });
    },
    shouldDownloadInvoice() {
        if (isFiscalPrinterActive(this.pos.config)) {
            // Never print invoice generated by odoo. It will be printed by the fiscal printer.
            return false;
        } else {
            return super.shouldDownloadInvoice(...arguments);
        }
    },
});
